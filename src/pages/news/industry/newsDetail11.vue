		<p class="p1">（五）拖欠住房公积金贷款本息3期以上,将直接划取贷款申请人个人公积金账户资金，用于归还贷款本息；</p>
		<p class="p1">（六）对于住房公积金贷款逾期4期以上的，将锁定其住房公积金个人账户，以限制其住房公积金的提取和转移；</p>
		<p class="p1">（七）法律、法规、规章规定的其他惩戒方式。</p>
		<p class="p1">对个人严重失信行为，采取下列方式予以惩戒：</p>
		<p class="p1">（一）录入住房公积金信息管理系统，记入失信个人黑名单，记录期限为5年，列为重点监控和监督检查对象；</p>
		<p class="p1">（二）对于取得住房公积金贷款后，违反规定停止缴存住房公积金或降低缴存住房公积金6个月以上的，公积金中心有权解除住房公积金贷款合同，提前收回贷款本息或按照商业住房贷款利率加收利息；</p>
