		<p class="p1">为保障住房公积金制度的有序运行，维护住房公积金缴存人的合法权益，倡导个人诚实守信，惩戒失信行为，营造良好的住房公积金使用环境，芜湖市住房公积金管理委员会三届九次全体委员会议讨论通过了《芜湖市住房公积金个人失信行为管理办法》，该办法将从2016年11月1日起正式实施。</p>
