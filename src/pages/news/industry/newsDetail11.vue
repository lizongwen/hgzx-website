		<p class="p1">失信行为的认定</p>
		<p class="p1">个人失信行为按照严重程度从低到高划分为三个等级，分别是一般失信行为、较重失信行为和严重失信行为。</p>
		<p class="p1">一般失信行为是指有以下情形之一的：拖欠住房公积金贷款本息2期的；取得住房公积金贷款后，违反规定停止缴存住房公积金或降低缴存住房公积金1个月以上3个月以下的；</p>
		<p class="p1">较重失信行为是指有以下情形之一的：以虚构、隐瞒事实，提供虚假伪造材料等欺诈手段申请住房公积金贷款或申请提取住房公积金未遂，经批评教育后承认错误的；以虚构、隐瞒事实，提供虚假伪造材料等欺诈手段获得了住房公积金贷款或提取了住房公积金，审查发现后经批评教育在10个工作日内全额退还的；拖欠住房公积金贷款本息3期以上6期以下的；取得公积金贷款后，违反规定停止缴存住房公积金或降低缴存住房公积金3个月以上6个月以下的；１年内发生2次一般失信行为的;</p>
		<p class="p1">严重失信行为是指有以下情形之一的：以虚构、隐瞒事实，提供虚假伪造材料等欺诈手段申请住房公积金贷款或申请提取住房公积金未遂，拒不承认错误的；以虚构、隐瞒事实，提供虚假伪造材料等欺诈手段获得了住房公积金贷款或提取了住房公积金，审查发现后拒绝退还贷款本息或提取的住房公积金的；取得住房公积金贷款后，违反规定停止缴存住房公积金或降低缴存住房公积金6个月以上的；拖欠住房公积金贷款本息6期以上的；拖欠住房公积金贷款本息，经催收未及时还款，被公积金中心起诉的；1年内出现3次以上一般失信行为，或2次以上较重失信行为的；</p>
		<p class="p1">失信行为的惩戒</p>
		<p class="p1">对个人一般失信行为，采取下列方式予以惩戒，督促其停止失信行为并进行整改。</p>
