		<p class="p1"> 对个人较重失信行为，采取下列方式予以惩戒：</p>
		<p class="p1">（一）录入住房公积金信息管理系统，记入失信个人黄名单，记录期限为2年，作为日常监督检查或抽查的重点；</p>
		<p class="p1">（二）书面通知所在单位；</p>
		<p class="p1">（三）取消失信个人2年内申请住房公积金贷款和提取资格；</p>
