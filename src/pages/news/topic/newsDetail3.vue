			<p class="p1">十年来，人民银行认真贯彻党中央、国务院部署，推动征信业不断发展。一是征信法规制度建设取得重大进展。《征信业管理条例》发布实施，征信业发展走上了有法可依的轨道。《条例》确立了征信业务及其相关活动所遵循的制度规则，从法律层面明确了人民银行及其派出机构对征信业的监督管理职责和管理手段，为实现对征信业务的常态化管理提供了法制保障。二是金融信用信息基础数据库建成并稳定运行。截至 2012 年 12 月底，金融信用信息基础数据库已为 8.2 亿自然人和 1859.6 万户企业建立了信用档案，为商业银行等机构防范信贷风险提供了重要支持。三是征信市场初步形成。信用评级、信用调查等征信机构迅速发展，社会对征信产品的需求日益增加。四是征信管理逐步加强和规范。征信管理体系不断健全，征信机构运营日趋规范，信用信息主体合法权益得到有效保护。五是征信宣传教育体系的长效机制初步建立，信用意识深入人心。六是牵头社会信用体系建设部际联席会议，推动社会信用体系建设不断取得进展，大力推进小微企业和农村信用体系建设。当前，征信业面临前所未有的机遇，我国社会信用体系建设也在深入推进。从国内看，党的十八届三中全会提出，&ldquo;要建立健全社会征信体系，褒扬诚信，惩戒失信。&rdquo;全社会对征信业的重视程度已经上升到一个新的高度，推动征信业发展，为信用活动提供有效的信用信息支持，构建诚实守信的社会氛围和环境，成为摆在我们面前的迫切任务；从世界范围看，国际金融危机后，各国普遍意识到信用信息服务对经济金融运行的深刻影响，其作用从防范非系统性风险上升为防范系统性风险，推动征信业健康发展成为各国面临的共同任务。</p>
			<p class="p1">下一阶段，推进征信工作要立足于以下方面。一是进一步完善征信法规体系，要以《征信业管理条例》为法律基础，建立和完善保障征信市场健康运行、保护信息主体权益的多层次的征信法律制度体系。二是大力发展征信市场体系，初步建立符合社会主义市场经济发展要求，竞争有序、运行安全、功能完善的征信市场体系。三是健全征信管理体系，要按照促进征信业规范可持续发展，有效保护信息主体合法权益的监管要求，加强征信市场IV管理，形成分类监管机制。四是探索建立征信宣传教育体系，紧紧围绕征信管理中心工作，坚持正面宣传为主的方针，积极创新工作机制，大力普及征信知识，推进征信国民教育。五是构建征信文化体系，为征信事业蓬勃健康发展提供精神动力和源泉。六是推进社会信用体系建设，要以健全信用法规、制度和标准化体系为基础，以推进行业信用建设、地方信用建设和征信市场发展为重点，推动社会信用体系建设,在全社会形成诚实守信的良好氛围和环境。</p>
