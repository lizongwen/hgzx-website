			<p class="p1">内容摘要</p>
			<p class="p1">征信是指依法收集、整理、保存、加工自然人、法人及其他组织的信用信息，并对外提供信用报告、信用评估、信用信息咨询等服务，帮助客户判断、控制信用风险，进行信用管理的活动。</p>
			<p class="p1">在我国，征信一词源于《左传&middot;昭公八年》中的&ldquo;君子之言，信而有征，故怨远于其身&rdquo;。其中，&ldquo;信而有征&rdquo;即为可验证其言为信实，或征求、验证信用。</p>
			<p class="p1">改革开放以来，中国经济社会经历了前所未有的大发展。社会主义市场经济框架逐步确立，多种所有制经济得以共同发展，市场在资源配置中日益发挥基础性作用，中国经济更为广泛和深刻地融入国际经济之中。金融领域内，中央银行调控体系和金融监管体系在调整中不断完善，银行的商业化运作模式由法律确认并通过股份制改造而进一步深化，多层次的金融市场更加均衡地发展，金融服务为实体经济提供了有力的支撑。正是在这样的背景下，市场、金融与信用紧密地联系在一起，现代意义上的中国征信业应运而生。</p>
			<p class="p1">党中央、国务院高度重视征信、信用体系建设，作出了一系列重大决策部署。2003 年 9 月，国务院明确赋予人民银行&ldquo;管理信贷征信业，推动社会信用体系&rdquo;的职责，人民银行设立了征II信管理局。2003 年 10 月，党的十六届三中全会《中共中央关于完善社会主义市场经济体制若干问题的决定》明确要求要&ldquo;加快建设企业和个人信用服务体系&rdquo;。2004 年，全国银行、证券、保险工作会议强调我国社会信用体系建设应从&ldquo;信贷信用征信起步&rdquo;，要&ldquo;加快全国统一的企业和个人信用信息基础数据库建设，形成覆盖全国的基础信用信息服务网络&rdquo;。</p>
